/* Define FONT TYPES

   - boldness: bit 1
   - italics : bit 2

   FONT_TYPE_PRIV_BOLD, FONT_TYPE_PRIV_ITALIC and FONT_TYPE_PRIV_BOLD_ITALIC
   defines are used both as separate font types and as flags for regular,
   semibold and plain fonts.

   Bit 0 is for antialiasing.

   We can use at most 8 bits (font cache key limitation).

   In other words, the bits are:
   ---------------------------------
   | 7 | 6 | 5 | 4 | 3 | 2 | 1 | 0 |
   ---------------------------------
   | t | t | t | t | t | i | b | a |
   ---------------------------------

   t: type bit
   i: italic effect
   b: bold effect
   a: antialiasing

*/

